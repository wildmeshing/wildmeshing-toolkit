void TriangleQuadrature::transformed_triangle_quadrature(
    const int order,
    const TriangleVertices& triangle,
    Quadrature& quadr)
{
    reference_triangle_quadrature(order, quadr);

    Eigen::Affine2d transform;
    transform.linear().col(0) = (triangle.row(1) - triangle.row(0)).transpose();
    transform.linear().col(1) = (triangle.row(2) - triangle.row(0)).transpose();
    transform.translation() = triangle.row(0).transpose();

    quadr.points() = (transform * quadr.points().leftCols<2>().transpose()).transpose();
    quadr.weights() *= transform.linear().determinant();
}

void TriangleQuadrature::reference_triangle_quadrature(const int order, Quadrature& quadr)
{
    quadr.set_dimension(2);
    get_weight_and_points(order, quadr);

    assert(fabs(quadr.weights().sum() - 1) < 1e-14);
    assert(quadr.points().minCoeff() >= 0 && quadr.points().maxCoeff() <= 1);

    assert(quadr.points().rows() == quadr.weights().size());

    // Reference triangle has area 0.5.
    quadr.weights() /= 2;
}

